#include "../includes/libft.h"

int		ft_rgb_to_int(t_rgb rgb)
{
	int rs;

	rs = rgb.r;
	rs <<= 8;
	rs += rgb.g;
	rs <<= 8;
	rs += rgb.b;
	return (rs);
}
