#include "../includes/libft.h"

size_t	ft_strlcat(char *dest, const char *src, size_t count)
{
	char		*d;
	const char	*s;
	size_t		n;
	size_t		dlen;

	d = dest;
	s = src;
	n = count;
	while (*d != '\0' && n-- != 0)
		d++;
	dlen = d - dest;
	n = count - dlen;
	if (n == 0)
		return (dlen + ft_strlen(s));
	while (*s != '\0')
	{
		if (n != 1)
		{
			*d++ = *s;
			n--;
		}
		s++;
	}
	*d = '\0';
	return (dlen + (s - src));
}
