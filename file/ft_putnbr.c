#include "../includes/libft.h"

void	ft_putnbr(int n)
{
	if (n == -2147483647 - 1)
		ft_putstr("-2147483648");
	else if (n < 0)
	{
		ft_putchar('-');
		ft_putnbr(-n);
	}
	else if (n >= 10)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	else
		ft_putchar((char)n + '0');
}
