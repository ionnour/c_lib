#include "../includes/libft.h"

int		ft_is(const char *str, int (*f)(char))
{
	size_t	i;

	if (!str)
		return (0);
	i = 0;
	while (str[i])
	{
		if (!f(str[i]))
			return (0);
		i++;
	}
	return (1);
}
